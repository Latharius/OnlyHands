import mongoose, { Schema } from 'mongoose'

const loginSchema = new Schema({
    username: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    gender: {
        type: String,
        required: true
    }
})

module.exports = new mongoose.model('Login', loginSchema)